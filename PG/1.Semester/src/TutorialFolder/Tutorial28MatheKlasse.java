package TutorialFolder;

public class Tutorial28MatheKlasse {

	public static void main(String[] args) {
		
		double wert = Math.ceil(25.3);	//Zahl wird aufgerundet 26
		
		Math.floor(8.9); 				//Zahl wird abgerundet 8 (punkt statt komma!)
		
		Math.abs(-15);					// abs macht den Abstand des wertes zum ursprung 0. als0 15
		
		Math.max(8.0, 2.3);				//max gibt den höhren wert der beiden aus. Also 8.0 weil der andere klein		
		
		Math.pow(3,5);					//pow exponentiert. also 3^5= 243.0
		
		Math.sqrt(25);					// sqrt heißt wurzel. Wurzel aus 25
		
		wert = Math.PI;					// berechnung für Pi
		System.out.println(wert);
		
						// Math. zeigt weitere Mathematische operatoren an!
	}

}
